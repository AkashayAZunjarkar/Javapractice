package loopUse;

public class Whileloop {

	public static void main(String[] args)
	{
		//i want to print table of 5
		//initialization condition updation
		// i=5; i=i<=50; i=i+5;
		
		int i=5;  //initilization
		while(i<=50)  //condition
		{
			System.out.println(i);
			i=i+5;  //updation
		}
		
		System.out.println("--------------------------------------");
		
		//i want to print numbers from 1 to 10
		//initialization condition updation
		// int m=1; m<=10 ; m=m+1;
		
		int m=1;
		while(m<=10)
		{
			System.out.println(m);
			m=m+1;
		}
		
		System.out.println("--------------------------------------");
		
		
		//I want table of 10 in reverse order
		//intilazation  condition updation
		//int s=100; s=s=>10;s=s-10
		
		int s=100;
		while(s>=10)
		{
			System.out.println(s);
			s=s-10;
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
