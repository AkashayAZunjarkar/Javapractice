package CastingStudy;

public class CastingEg {

	public static void main(String[] args)
	{
		int a=99;
		double b;
		b=a;
		System.out.println(b);
		
		
		System.out.println("===========explict casting==================");
		
		double c=999.99d;
		int d;
		d=(int) c;
		System.out.println(d);
		
		

	}

}
